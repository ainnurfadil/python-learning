nama = input("Nama kamu siapa? ")

# Pengecekan nama dengan if, elif, dan else
if nama == "Ucup": # kondisi satu
    print("Hai ganteng bet") # aksi true satu
elif nama == "Otong": # kondisi dua
    print("Hai si kece banget") # aksi true dua
elif nama == "Mario": # kondisi tiga
    print("Hai humoris") # aksi true tiga
else:
    print("Awak gak kenal") # aksi false terakhir
